function buildSavingsRandomMap() {
  const slots = [3, 5, 8, 11, 14, 17, 20, 23, 26];
  const shuffled = [...slots].sort(() => Math.random() - 0.5);
  const chosenSlots = shuffled.slice(0, 5);
  const eventIndices = Array.from({ length: 5 }, () => Math.floor(Math.random() * SAVINGS_RANDOM_EVENTS.length));
  const map = {};
  chosenSlots.forEach((dayIdx, i) => {
    const ev = SAVINGS_RANDOM_EVENTS[eventIndices[i]];
    map[dayIdx] = { ...ev, day: dayIdx + 1 };
  });
  return map;
}

export default function ScenarioSavings({
  apiBase,
  apiFetch,
  user,
  scenario,
  difficulty,
  budgetBump,
  onBudgetBump,
  onBackToMap,
}) {
  const maxDays = scenario.maxDays || 30;
  const goal = scenario.goal || 5000;
  const [loading, setLoading] = useState(true);
  const [saveInfo, setSaveInfo] = useState('');
  const [dayIndex, setDayIndex] = useState(0); // индекс в DAYS
  const [budget, setBudget] = useState(scenario.baseBudget || 1000);
  const [earned, setEarned] = useState(0);
  const [spent, setSpent] = useState(0);
  const [comment, setComment] = useState('');
  const [finished, setFinished] = useState(false);
  const [resultText, setResultText] = useState('');
  const [showHints, setShowHints] = useState(true);
  const [phase, setPhase] = useState('context'); // context -> choice -> result
  const [typedText, setTypedText] = useState('');
  const [isTyping, setIsTyping] = useState(false);
  const [showUnlock, setShowUnlock] = useState(false);
  const [savingsRandomMap, setSavingsRandomMap] = useState({});
  const typingTimer = useRef(null);
  const fullTextRef = useRef('');

  const allBackgroundUrls = useMemo(
    () => [
      ...new Set([
        ...DAYS.map((d) => d.backgroundUrl),
        ...(SAVINGS_RANDOM_EVENTS || []).map((e) => e.backgroundUrl),
      ].filter(Boolean)),
    ],
    []
  );
  useEffect(() => {
    allBackgroundUrls.forEach((url) => {
      const img = new Image();
      img.src = url;
    });
  }, [allBackgroundUrls]);

  const currentEvent = (savingsRandomMap[dayIndex] ?? DAYS[dayIndex]) || DAYS[DAYS.length - 1];
  const currentDay = currentEvent.day;
  const isExpert = difficulty === 'expert';

  const characterName = user.name || user.login || 'Игрок';

  const startTypewriter = (text) => {
    if (typingTimer.current) {
      clearInterval(typingTimer.current);
      typingTimer.current = null;
    }
    fullTextRef.current = text;
    setTypedText('');
    setIsTyping(true);
    let i = 0;
    typingTimer.current = setInterval(() => {
      i += 1;
      setTypedText(text.slice(0, i));
      if (i >= text.length) {
        clearInterval(typingTimer.current);
        typingTimer.current = null;
        setIsTyping(false);
      }
    }, 40);
  };

  const revealAll = () => {
    if (!isTyping) return;
    if (typingTimer.current) {
      clearInterval(typingTimer.current);
      typingTimer.current = null;
    }
    setTypedText(fullTextRef.current);
    setIsTyping(false);
  };

  useEffect(() => {
    let alive = true;
    async function initRun() {
      try {
        setLoading(true);
        // 1) Попробуем найти активный run
        const r1 = await apiFetch(`${apiBase}/runs/${scenario.code}`);
        let run = r1.ok ? await r1.json() : null;

        // 2) Если нет — создаём
        if (!run) {
          const r2 = await apiFetch(`${apiBase}/runs/start`, {
            method: 'POST',
            body: JSON.stringify({ scenarioCode: scenario.code }),
          });
          run = await r2.json();
        }

        if (!alive) return;
        const state = run.state || {};
        let nextDayIndex = Number(run.dayIndex || 0);
        let nextBudget = Math.round(Number.isFinite(run.budget) ? run.budget : scenario.baseBudget || 1000);
        let nextEarned = Math.round(Number(run.earned || 0));
        let nextSpent = Math.round(Number(run.spent || 0));
        setDayIndex(nextDayIndex);
        setBudget(nextBudget);
        setEarned(nextEarned);
        setSpent(nextSpent);
        setComment(state.comment || '');
        setSavingsRandomMap(state.savingsRandomMap && Object.keys(state.savingsRandomMap).length > 0 ? state.savingsRandomMap : buildSavingsRandomMap());
        setPhase('context');
      } finally {
        if (alive) setLoading(false);
      }
    }

    initRun();
    return () => {
      alive = false;
    };
  }, [apiBase, apiFetch, scenario.code, scenario.baseBudget]);

  useEffect(() => {
    // При смене дня — заново проигрываем контекст и прячем выбор до конца текста
    setPhase('context');
    setComment('');
    startTypewriter(`${currentEvent.title}\n\n${currentEvent.text}`);
    return () => {
      if (typingTimer.current) clearInterval(typingTimer.current);
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [dayIndex]);

  const saveRun = async ({ nextDayIndex, nextBudget, nextEarned, nextSpent, nextComment }) => {
    try {
      setSaveInfo('Сохраняем...');
      const res = await apiFetch(`${apiBase}/runs/save`, {
        method: 'POST',
        body: JSON.stringify({
          scenarioCode: scenario.code,
          dayIndex: nextDayIndex,
          budget: nextBudget,
          earned: nextEarned,
          spent: nextSpent,
          state: { comment: nextComment || '', savingsRandomMap },
        }),
      });
      if (res.ok) setSaveInfo('Сохранено');
      else setSaveInfo('');
    } catch {
      setSaveInfo('');
    } finally {
      setTimeout(() => setSaveInfo(''), 900);
    }
  };

  const finishScenario = async (finalBudget, finalEarned, finalSpent) => {
    const status = finalBudget >= goal ? 'passed' : 'failed';
    const text =
      status === 'passed'
        ? `Ты накопил(а) ${finalBudget} руб. и смог(ла) купить велосипед! Отличная работа!`
        : `Ты накопил(а) ${finalBudget} руб. — до велосипеда не хватило. Но ты уже знаешь, как в следующий раз тратить ещё осознаннее.`;

    setFinished(true);
    setResultText(text);
    setPhase('result');
    startTypewriter(text);

    try {
      await apiFetch(`${apiBase}/runs/finish`, {
        method: 'POST',
        body: JSON.stringify({
          scenarioCode: scenario.code,
          status,
          finalBudget,
          earned: finalEarned,
          spent: finalSpent,
        }),
      });
    } catch {}

    if (status === 'passed') {
      setTimeout(() => setShowUnlock(true), 450);
    }
  };

  const speakText = (text) => {
    if (!window.speechSynthesis) return;
    window.speechSynthesis.cancel();
    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = 'ru-RU';
    window.speechSynthesis.speak(utterance);
  };

  const handleChoice = (choice) => {
    const delta = choice.delta || 0;
    const newBudget = Math.round(budget + delta);
    const newEarned = Math.round(earned + (delta > 0 ? delta : 0));
    const newSpent = Math.round(spent + (delta < 0 ? Math.abs(delta) : 0));
    setBudget(newBudget);
    setEarned(newEarned);
    setSpent(newSpent);
    const commentLine = choice.comment || '';
    const outroLine = choice.outro ? `\n\n💡 ${choice.outro}` : '';
    setComment(commentLine + outroLine);
    setPhase('result');
    startTypewriter(commentLine + outroLine);
    onBudgetBump();

    const nextIndex = dayIndex + 1;
    if (nextIndex >= maxDays || nextIndex >= DAYS.length) {
      // финальный день: закрываем run и начисляем достижения
      finishScenario(newBudget, newEarned, newSpent);
    } else {
      // После результата дадим кнопку "Дальше"
      setTimeout(() => {
        setDayIndex(nextIndex);
      }, 0);
      // сохраняем так, чтобы при возврате продолжить со следующего дня
      saveRun({
        nextDayIndex: nextIndex,
        nextBudget: newBudget,
        nextEarned: newEarned,
        nextSpent: newSpent,
        nextComment: choice.comment || '',
      });
    }
  };

  const exitScenario = async () => {
    await saveRun({
      nextDayIndex: dayIndex,
      nextBudget: budget,
      nextEarned: earned,
      nextSpent: spent,
      nextComment: comment,
    });
    onBackToMap();
  };

  if (loading) {
    return <div className="text-muted">Загружаем сохранение...</div>;
  }

  const leftHud = (
    <>
      <div className="hud-pill">
        <span className="hud-label">День</span>
        <span className="hud-value">
          {currentDay}/{maxDays}
        </span>
      </div>
      <div className="hud-pill">
        <span className="hud-label">💰</span>
        <span className={`hud-value ${budgetBump ? 'fade-number bump' : ''}`}>{Math.round(budget)}</span>
      </div>
    </>
  );

  // Путь к картинке фона: положи файл в frontend/public/backgrounds/ и пропиши здесь:
  // backgroundUrl: '/backgrounds/your-file.jpg'
  const bgUrl = currentEvent.backgroundUrl || null;

  return (
    <ScenarioStage backgroundUrl={bgUrl} backgroundClassName={currentEvent.backgroundClassName} leftHud={leftHud} onExit={exitScenario}>
      {showUnlock && (
        <div className="unlock-overlay" onClick={() => setShowUnlock(false)}>
          <div className="unlock-card" onClick={(e) => e.stopPropagation()}>
            <div className="unlock-item">🚲</div>
            <div style={{ fontWeight: 900, fontSize: '1.2rem' }}>Достижение получено!</div>
            <div className="text-muted" style={{ marginTop: 8 }}>
              «Новый велосипед» добавлен в ваш профиль (витрина достижений).
            </div>
            <div style={{ display: 'flex', gap: 10, marginTop: 14 }}>
              <button className="primary-btn" type="button" onClick={() => { setShowUnlock(false); onBackToMap(); }}>
                К сценариям
              </button>
              <button className="secondary-btn" type="button" onClick={() => setShowUnlock(false)}>
                Продолжить смотреть
              </button>
            </div>
          </div>
        </div>
      )}

      <div className="character-layer">
        {/* сюда позже можно подставлять png персонажей, когда ты загрузишь */}
        {/* пример: <img className="character-sprite left" src="/characters/katya.png" alt="" /> */}
      </div>

      <div className="dialog-area">
        <div className="dialog-box" onClick={revealAll} role="button" tabIndex={0}>
          <div className="speaker-row">
            <div className="speaker-name">{characterName}</div>
            <div className="text-muted" style={{ fontSize: '0.82rem' }}>
              {saveInfo || (isTyping ? 'Нажми, чтобы ускорить' : '')}
            </div>
          </div>
          <div className="dialog-text">
            {typedText}
            {isTyping && <span className="caret" />}
          </div>
        </div>

        {!finished && (
          <div className="dialog-box">
            {phase === 'context' && (
              isTyping ? (
                <div className="choice-ghost">Выбор появится после того, как ты дочитаешь текст.</div>
              ) : (
                <div className="choices-row">
                  <div className="dialog-choice-intro text-muted" style={{ marginBottom: 6 }}>
                    Теперь выбери действие на этот день:
                  </div>
                  {currentEvent.choices.map((choice, idx) => (
                    <div key={idx}>
                      <button
                        type="button"
                        className="primary-btn choice-btn"
                        onClick={() => handleChoice(choice)}
                      >
                        <span>{choice.label}</span>
                        {!isExpert && choice.delta !== 0 && (
                          <span
                            className={`choice-delta ${choice.delta > 0 ? 'choice-delta--plus' : 'choice-delta--minus'}`}
                            style={{ fontVariantNumeric: 'tabular-nums', fontSize: '0.9rem', fontWeight: 700 }}
                          >
                            {choice.delta > 0 ? '+' : ''}
                            {choice.delta}
                          </span>
                        )}
                      </button>
                      {!isExpert && showHints && choice.hint && (
                        <div
                          className="choice-hint"
                          style={{ fontSize: '0.85rem', marginLeft: 6, marginTop: 4 }}
                        >
                          Подсказка: {choice.hint}
                        </div>
                      )}
                    </div>
                  ))}
                  <div style={{ display: 'flex', gap: 10, marginTop: 10 }}>
                    {!isExpert && (
                      <button
                        className="secondary-btn"
                        type="button"
                        onClick={() => setShowHints((p) => !p)}
                      >
                        {showHints ? 'Скрыть подсказки' : 'Показать подсказки'}
                      </button>
                    )}
                    <button
                      className="secondary-btn"
                      type="button"
                      onClick={() => speakText(`${currentEvent.title}. ${currentEvent.text}`)}
                    >
                      🔊 Озвучить
                    </button>
                  </div>
                </div>
              )
            )}

            {phase === 'result' && (
              <div className="choices-row">
                <div className="text-muted">
                  {isTyping ? '...' : 'Готово. Переходим дальше.'}
                </div>
              </div>
            )}
          </div>
        )}

        {finished && (
          <div className="dialog-box">
            <div className="choices-row">
              <button className="primary-btn" type="button" onClick={onBackToMap}>
                Вернуться к сценариям
              </button>
            </div>
          </div>
        )}
      </div>
    </ScenarioStage>
  );
}

